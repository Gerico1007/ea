
// l'exemple suivant montre comment créer une fonction simple en JavaScript qui prend un nom en entrée et renvoie un message de salutation.
// Node.js is a runtime environment that allows you to run JavaScript code outside of a web browser. It is commonly used for building server-side applications and scripts.

// JavaScript
function greet(name) {
    return `Hello, ${name}!`;
}

console.log(greet("Alice"));
console.log(greet("Bob"));
console.log(greet("Charlie"));

// after running the code, with the command node javascript.js, you should see the following output:
// Hello, Alice!
// Hello, Bob!
// Hello, Charlie